    private void btnAñadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAñadirActionPerformed
        boolean validar = validarTxtPersona();
        
        if (validar) {
            int id = Integer.parseInt(txtIdAñadir.getText());
            String nombre = txtNombre.getText();
            int edad = Integer.parseInt(txtEdad.getText());
            String correo = txtCorreo.getText();
            boolean divorciado;

            if (rbTrue.isSelected()) {
                divorciado = true;
            } else {
                divorciado = false;
            }

            Persona p = new Persona(id, nombre, edad, correo, divorciado);

            DAOPersona.getInstance().insertarPersona(p);
            JOptionPane.showMessageDialog(this, "Persona añadida correctamente");
            limpiarTxtPersona();
        } else {
            JOptionPane.showMessageDialog(this, "Hubo un error al añadir la persona");
        }
    }//GEN-LAST:event_btnAñadirActionPerformed

    private boolean validarTxtPersona() {
        if(txtIdAñadir.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Introduzca el id.");
            return false;
        } else if (txtNombre.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Introduzca el nombre.");
            return false;
        } else if (txtEdad.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Introduzca la edad.");
            return false;
        } else if (txtCorreo.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Introduzca el correo.");
            return false;
        } else if (!rbTrue.isSelected() && !rbFalse.isSelected()) {
            JOptionPane.showMessageDialog(null, "Introduzca si esta divorciado.");
            return false;
        }
        return true;
    } 
    
    private void limpiarTxtPersona() {
        txtIdAñadir.setText("");
        txtNombre.setText("");
        txtEdad.setText("");
        txtCorreo.setText("");
        rbTrue.setEnabled(false);
        rbFalse.setEnabled(false);
    }
    
    
    private void btnBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarActionPerformed
        boolean validar = txtValidarBorrar();
        
        if (validar) {
            int id = Integer.parseInt(txtIdBorrar.getText());
            
            DAOPersona.getInstance().borrarPersona(id);
            JOptionPane.showMessageDialog(this, "Persona borrada correctamente");
            limpiarTxtBorrar();
        } else {
            JOptionPane.showMessageDialog(this, "Hubo un error al borrar");
        }
        
        
        
    }//GEN-LAST:event_btnBorrarActionPerformed

    private boolean txtValidarBorrar() {
        if (txtIdBorrar.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Introduzca el id.");
            return false;
        }
        return true;
    }
    
    private void limpiarTxtBorrar() {
        txtIdBorrar.setText("");
    }

