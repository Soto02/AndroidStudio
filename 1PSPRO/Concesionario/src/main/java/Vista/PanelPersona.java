    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        try {
            if (txtConsulta.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Esta vacio el buscador");
            } else {
                Controlador.cargarTablaConsulta(tablePersonas, Servicio.getInstance().getPersonasP(txtConsulta.getText()));
            }
        } catch (SQLException ex) {
            Logger.getLogger(PanelPersona.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_btnBuscarActionPerformed

